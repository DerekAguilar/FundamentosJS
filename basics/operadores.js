


// Operadores Matemáticos
// + - * / % **
// += -= *= /= %= **=
console.log(10 + 10); // Suma
console.log(10 - 10); // Resta
console.log(10 * 10); // Multiplicación
console.log(10 / 10); // División
console.log(10 % 10); // Módulo o residuo
console.log(10 ** 10); // Potencia

let num = 15;
console.log(num**=5);
console.log(num+=5); // num = num + 5
console.log(num-=5); // num = num - 5
console.log(num*=5); // num = num * 5
console.log(num/=5); // num = num / 5
console.log(num%=5); // num = num % 5

// Operadores booleanos
// > < >= <= !== ===
console.log(1 > 2);
console.log(1 < 2);
console.log(1 >= 2);
console.log(1 <= 2);
console.log(1 !== 2);
console.log(1 === 2);

// Operadores lógicos
// and &&
console.log(true && true);
// or ||
console.log(true || false);
// not !
console.log(!true);